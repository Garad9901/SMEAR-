import axios from 'axios';

// Pull from environment or fallback to default FastAPI dev port
const API_BASE_URL = import.meta.env.VITE_API_BASE_URL || 'http://localhost:8000';

const api = axios.create({
  baseURL: `${API_BASE_URL}/api/v1`,
  headers: {
    'Content-Type': 'application/json',
  },
});

// Auto-inject JWT token from localStorage into Bearer header
api.interceptors.request.use(
  (config) => {
    const token = localStorage.getItem('agrosense_jwt');
    if (token && config.headers) {
      config.headers.Authorization = `Bearer ${token}`;
    }
    return config;
  },
  (error) => {
    return Promise.reject(error);
  }
);

// Graceful global error handling
api.interceptors.response.use(
  (response) => response,
  (error) => {
    const status = error.response ? error.response.status : null;
    if (status === 401) {
      // Auto-logout user if token expires or is invalid
      localStorage.removeItem('agrosense_jwt');
      localStorage.removeItem('agrosense_user');
      if (window.location.pathname !== '/login' && window.location.pathname !== '/register') {
        window.location.href = '/login';
      }
    }
    return Promise.reject(error);
  }
);

export default api;
